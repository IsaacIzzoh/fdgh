// Get the current URL
var currentUrl = window.location.href;
// console.log('Current URL:', currentUrl);

// Get all the anchor tags inside the menu with the class 'menuzord-menu'
var menuLinks = document.querySelectorAll('.menu-links');

// Flag to check if an active link has been found
var activeFound = false;

// Loop through each menu link and compare with the current URL
for (var i = 0; i < menuLinks.length; i++) {
  var link = menuLinks[i];
  var linkUrl = link.getAttribute('href');
 // console.log('Link URL:', linkUrl);

  // Check if the current URL ends with the link URL
  if (currentUrl.endsWith(linkUrl)) {
    // Add a class to the parent li to highlight the active page
    link.parentElement.classList.add('active');

    // If it's a dropdown item, also highlight the parent
    var parentUl = link.closest('ul_drop');
    if (parentUl) {
      parentUl.parentElement.classList.add('active');
    }

    // Set the flag to indicate an active link was found
    activeFound = true;
    break; // No need to continue checking other links
  }
}


// If no active link was found, set "Home" link as active by default
if (!activeFound) {
  // Assuming that the "Home" link has an href attribute set to "./"
  var homeLink = document.querySelector('.menu-links a[href="./"]');
  if (homeLink) {
    homeLink.parentElement.classList.add('active');
  }
}

